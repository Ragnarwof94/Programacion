#➡️ Recorrer una lista con un bucle for
#➡️ Ejemplo de un ciclo for en Python
#➡️ La variable fruta toma el valor de cada elemento en la lista durante cada iteración del ciclo
#➡️ El ciclo for se repite hasta que se hayan recorrido todos los elementos en la lista
#➡️ Inicialización de la lista de frutas
lista = ["manzana", "banana", "cereza"]
#➡️ Ciclo for para iterar sobre cada fruta en la lista
#➡️ Imprime cada fruta
for fruta in lista:
    #➡️ Imprime la fruta actual
    #➡️ La variable fruta toma el valor de cada elemento en la lista durante cada iteración
    #➡️ El ciclo se repite hasta que se hayan recorrido todos los elementos en la lista
    print(fruta)