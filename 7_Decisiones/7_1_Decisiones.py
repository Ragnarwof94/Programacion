# ➡️ Solicitar al usuario su edad
edad = input("Ingrese su edad: ")
# ➡️ Convertir la entrada de texto a un número entero
edad = int(edad)
# ➡️ Verificar si la edad es mayor a 18
if edad > 18:
    # ➡️ Si la condición es verdadera, imprimir "Si es mayor"
    print("Si es mayor")
# ➡️ Si la condición es falsa, imprimir "No es mayor"
else:
    # ➡️ Imprimir "No es mayor"
    print("No es mayor")
