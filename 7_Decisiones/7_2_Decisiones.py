# ➡️ Ejemplo de estructura condicional if-else
# ➡️ Se evalúa si la variable 'mascota' es igual a 'perro'
mascota = 'gato'
# ➡️ Usamos el operador de igualdad (==) para comparar valores
# ➡️ Si la condición es verdadera, se ejecuta el bloque de código indentado debajo del if
# ➡️ Si es falsa, se ejecuta el bloque debajo del else
# ➡️ Aquí comparamos el valor de la variable 'mascota' con el texto 'perro'
if mascota == 'perro':
    # ➡️ Si la condición es verdadera, se ejecuta este bloque
    print("Verdad")
    # ➡️ Si la condición es falsa, se ejecuta este otro bloque
else:
    print("Mentira")
