"""
Los booleanos son un tipo de dato que solo puede tener dos valores posibles: True (verdadero) o False (falso). Se utilizan comúnmente en lógica y condiciones para controlar el flujo de un programa.
"""

# Ejemplos de booleanos
# ➡️ Aquí se asignan valores booleanos a variables.
# ➡️ Los booleanos solo pueden ser True o False.
# ➡️ Se usan para representar condiciones como verdadero/falso, sí/no, encendido/apagado, etc.
# ➡️ Los booleanos son útiles en estructuras de control como condicionales y bucles.
# ➡️ No se usan comillas, ya que no son strings.
# ➡️ Los booleanos son sensibles a mayúsculas: True y False deben escribirse con la primera letra en mayúscula.

es_mayor = True
tiene_licencia = False
come_frutas = True

# ➡️ Impresión de los valores almacenados en las variables
# ➡️ Usamos print() para mostrar los valores en consola.
# ➡️ Cada print muestra el valor de una variable diferente.
# ➡️ Las comas en print() separan los elementos a imprimir.

print("¿Es mayor de edad?: ", es_mayor)
print("¿Tiene licencia de conducir?: ", tiene_licencia)
print("¿Come frutas regularmente?: ", come_frutas)