# ➡️ Reglas para nombrar variables en Python
# ➡️ Las variables deben comenzar con una letra (a-z, A-Z) o un guion bajo (_)
# ➡️ Después del primer carácter, pueden contener letras, números (0-9) o guiones bajos (_)
# ➡️  Las variables no pueden comenzar con un número
# ➡️  Las variables no pueden contener espacios
# ➡️ Las variables no pueden contener caracteres especiales como !, @, #, $, %, ^
# ➡️ Las variables no pueden ser palabras reservadas del lenguaje (como print, input, int, float, etc.)
# Las variables son sensibles a mayúsculas y minúsculas (por ejemplo, miVariable)
# ➡️ Ejemplos de nombres de variables válidos

miVariable = 20
MiVariable = 30
# Si se tienen varias palabras en una variable, se puede usar:
# ➡️ Notación camelCase
miVariableEjemplo = 40
# ➡️  Notación con guion bajo
mi_variable_ejemplo = 50