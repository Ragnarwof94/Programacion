"""
Los strings (cadenas de texto) son secuencias de caracteres que pueden incluir letras, números, espacios y símbolos. Se utilizan para representar texto en un programa.
"""

# Ejemplos de strings
# ➡️ Aquí se asignan valores de texto a variables.
# ➡️ Los strings se encierran entre comillas simples (' ') o dobles (" ").
# ➡️ Pueden contener letras, números, espacios y símbolos.
# ➡️ Los strings se usan para representar nombres, correos electrónicos, direcciones, etc.
# ➡️ Los strings pueden estar vacíos ("") o contener múltiples palabras.

nombre = "Carlos Andres"
email = "carlos123@gmail.com"
ciudad = "Bogotá"

# ➡️ Impresión de los valores almacenados en las variables
# ➡️ Usamos print() para mostrar los valores en consola.
# ➡️ Cada print muestra el valor de una variable diferente.
# ➡️ Las comas en print() separan los elementos a imprimir.
# ➡️ No se usan comillas aquí porque no son strings, sino variables.
# ➡️ Si se usaran comillas, se imprimiría el nombre de la variable en lugar de su valor.

print("El nombre es: "+ nombre)
print("El email es: "+ email)
print("La ciudad es: "+ ciudad)