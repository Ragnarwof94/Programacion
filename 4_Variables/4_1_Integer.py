"""
Los integeres (enteros) son números sin parte decimal, tanto positivos como negativos, incluyendo el cero.
"""

# Ejemplos de integeres
"""
Nombre de la variable, un igual y el valor integer
"""
# ➡️ Aquí se asignan valores enteros a variables.
edad = 25
year = 2025
# ➡️ También pueden ser negativos
temperatura = -10

# ➡️ Impresión de los valores almacenados en las variables
# ➡️ Usamos print() para mostrar los valores en consola.
# ➡️ Cada print muestra el valor de una variable diferente.
# ➡️ Las comas en print() separan los elementos a imprimir.
# ➡️ No se usan comillas aquí porque no son strings, sino números.
print("La edad es: ", edad)
print("El año es: ", year)
print("La temperatura es: ", temperatura)