"""
Los números float (de punto flotante) son números que incluyen una parte decimal. Pueden ser positivos o negativos, y también pueden representar valores muy grandes o muy pequeños.
"""
# Ejemplos de números float
# ➡️ Aquí se asignan valores float a variables.
# ➡️ Los float usan un punto (.) para separar la parte entera de la decimal.
# ➡️ Pueden representar cantidades con decimales, como pesos, precios, alturas, etc.
# ➡️ Los float pueden ser positivos o negativos.
gramos = 24.99
precio = 1999.95
altura = 1.75

# ➡️ Impresión de los valores almacenados en las variables
# ➡️ Usamos print() para mostrar los valores en consola.
# ➡️ Cada print muestra el valor de una variable diferente.
# ➡️ Las comas en print() separan los elementos a imprimir.

print("Los gramos son: ",gramos)
print("El precio es: ",precio)
print("La altura es: ",altura)