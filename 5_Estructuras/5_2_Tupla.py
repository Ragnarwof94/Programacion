"""
Tuplas en Python son estructuras de datos que permiten almacenar múltiples valores en una sola variable, similar a las listas. Sin embargo, a diferencia de las listas, las tuplas son inmutables, lo que significa que una vez creadas, sus elementos no pueden ser modificados, añadidos o eliminados. Las tuplas se definen utilizando paréntesis () y pueden contener elementos de diferentes tipos de datos.
"""

# Ejemplos de tuplas
#➡️ Tupla de coordenadas
coordenadas = (10.0, 20.0)
#➡️ Tupla de punto en 3D
punto_3D = (5, 10, 15)
